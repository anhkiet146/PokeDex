export function getItemDesc(itemName) {
  if (!itemName) return '';
  const key = itemName.toLowerCase().trim();
  return ITEM_DESCRIPTIONS[key] || 'Vật phẩm hỗ trợ Pokémon trong chiến đấu, giúp tăng chỉ số hoặc mang lại hiệu ứng đặc biệt.';
}

export function getMoveDesc(moveName) {
  if (!moveName) return '';
  const key = moveName.toLowerCase().trim().replace(/-/g, ' ');
  return MOVE_DESCRIPTIONS[key] || MOVE_DESCRIPTIONS[moveName.toLowerCase().trim()] || 'Chiêu thức tấn công hoặc hỗ trợ chiến thuật.';
}
