export function getReputableBuildSuggestions(pokemon) {
  const name = pokemon.name.toLowerCase().trim();
  
  // 1. Check exact metagame match
  if (METAGAME_BUILDS[name]) {
    return METAGAME_BUILDS[name];
  }
  
  // 2. Check mask or form variations
  const baseName = name.split('-')[0];
  if (METAGAME_BUILDS[baseName]) {
    return METAGAME_BUILDS[baseName];
  }

  // 3. Fallback to primary type build
  const primaryType = pokemon.types?.[0]?.toLowerCase() || 'default';
  if (GENERIC_TYPE_BUILDS[primaryType]) {
    return GENERIC_TYPE_BUILDS[primaryType];
  }

  return GENERIC_TYPE_BUILDS['default'];
}
